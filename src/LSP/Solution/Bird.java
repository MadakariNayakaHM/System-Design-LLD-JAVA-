package LSP.Solution;

public class Bird {
    String Weight;
    String Colour;
    String type;
    String Size;
    String BeakType;

    Bird()
    {

    }

    Bird(String weight, String Colour, String type, String Size, String BeakType)
    {
        this.Weight = weight;
        this.BeakType = BeakType;
        this.Colour = Colour;
        this.type = type;
        this.Size = Size;
    }

}
